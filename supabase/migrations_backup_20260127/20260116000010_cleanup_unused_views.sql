-- 미사용 VIEW 삭제
-- 작성일: 2026-01-16
-- 설명: 더 이상 사용하지 않는 파트너 정산 관련 VIEW 및 office 기반 VIEW 삭제
-- =====================================================

-- =====================================================
-- 1. 파트너 정산 관련 VIEW 삭제
-- 파트너 정산 기능이 삭제되어 더 이상 사용하지 않음
-- =====================================================
DROP VIEW IF EXISTS settlement_dashboard CASCADE;
DROP VIEW IF EXISTS partner_debt_status CASCADE;

-- =====================================================
-- 2. Office 기반 VIEW 삭제
-- office가 동적 값으로 변경되어 더 이상 사용하지 않음
-- =====================================================
DROP VIEW IF EXISTS payment_stats_by_office CASCADE;

-- =====================================================
-- 3. 사용하지 않는 레거시 VIEW 정리
-- =====================================================
-- 중복 정의된 VIEW들이 있다면 여기서 정리
DROP VIEW IF EXISTS case_summary CASCADE;
DROP VIEW IF EXISTS case_stats CASCADE;

